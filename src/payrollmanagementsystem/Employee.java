    public void EmployeeTable()
    {
        try{
            String sql="select Emp_ID,Emp_Dept,Emp_Name,Emp_Doj,Emp_Address,Emp_City,Emp_Nationality,Emp_Email,Emp_PhoneNo From EMPLOYEE";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
            
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
            System.out.println("Employee Table Error: "+ex);
        }
        finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }
