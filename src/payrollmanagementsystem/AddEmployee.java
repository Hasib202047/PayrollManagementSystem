    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql="INSERT INTO General_User(Email,User_Name,Password)  values(?,?,?)";
        try{
            
            pst=conn.prepareStatement(sql);
            
            pst.setString(1,jTextField1.getText());//Department ID
            pst.setString(2,jTextField2.getText());//Employee Name
            pst.setString(3,jTextField9.getText());//Date of Birth
            pst.setString(4,jTextField3.getText());//Age
            pst.setString(5,(String)jComboBox1.getSelectedItem());//Gender
            pst.setString(6,(String)jComboBox2.getSelectedItem());//Religion
            pst.setString(7,jTextField10.getText());//Date of Join
            pst.setString(8,jTextField4.getText());//Address
            pst.setString(9,jTextField5.getText());//City
            pst.setString(10,jTextField11.getText());//Nationality
            pst.setString(11,jTextField6.getText());//Email
            pst.setString(12,jTextField7.getText());//Phone
            pst.setString(13,jTextField8.getText());//Password
            
            pst.execute();
            JOptionPane.showMessageDialog(null, "Congratulation!\nNew Employee has been Added");
            
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
            System.out.println("AddEmployee Error: "+ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

